from typing import List
from queue import Queue
class Solution:
    #Function to return Breadth First Traversal of given graph.
    def bfsOfGraph(self, V: int, adj: List[List[int]]) -> List[int]:
        # code here
        queue=[0]
        visited=[0]*V
        m=[]
        while queue:
            curr=queue.pop(0)
            m.append(curr)
            for i in adj[curr]:
                if not visited[i]:
                    visited[i]=1
                    #m.append(curr)
                    queue.append(i)
                    
        return m   


